package com.example.lynn_books_recommendation;

public class Book {
    public String Title;
    public String Author;
    public String Pages;
    public String PublishedYear;
    public String Description;
    public String LinkBook;
    public String LinkCover;

    //Book constructor
    public Book (String Title,String Author,String Pages,String PublishedYear, String Description,String LinkBook,String LinkCover){
        this.Title = Title;
        this.Author = Author;
        this.Pages = Pages;
        this.PublishedYear = PublishedYear;
        this.Description = Description;
        this.LinkBook = LinkBook;
        this.LinkCover = LinkCover;

    }





}
